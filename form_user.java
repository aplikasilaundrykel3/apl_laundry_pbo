    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Anda yakin ingin menghapus data ini='" + txtid.getText()+ "'",".:: konfirmasi ::.", JOptionPane.YES_NO_OPTION);
        if (ok==0){
            try {
                PreparedStatement ps = con.prepareStatement("DELETE FROM user WHERE id_user=?");
                ps.setString(1, txtid.getText());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data telah di delete");
                tampil_tabel();
            } catch(Exception e) {
                System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        try{
            PreparedStatement ps = con.prepareStatement("UPDATE user SET nama=?, username=?, password=?, id_outlet=?, role=? WHERE id_user=?");
            ps.setString(1, txtnama.getText());
            ps.setString(2, txtusername.getText());
            ps.setString(3, txtpass.getText());
            ps.setString(4, cbidoutlet.getSelectedItem().toString());
            ps.setString(5, cbrole.getSelectedItem().toString());
            ps.setString(6, txtid.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data telah di update");
            tampil_tabel();
            
        }catch(Exception e){
            System.out.print("ERROR KUERI KE DATABASE:\n"+ e +"\n\n");
        }
    }//GEN-LAST:event_btneditActionPerformed

