    public void cbidoutlet(){
        try {
            PreparedStatement s=con.prepareStatement("SELECT id_outlet FROM outlet");
            ResultSet r=s.executeQuery();
            while (r.next()){
                cbidoutlet.addItem(r.getString("id_outlet"));
            }
        } catch (SQLException e){
        }
    }
    
    
    private void tampil_tabel(){
        tm=new DefaultTableModel (null,
        new Object[]{"id_user","nama","username","password","id_outlet","role"});
        tbluser.setModel(tm);
        tm.getDataVector().removeAllElements();
        try{
            PreparedStatement s=con.prepareStatement("SELECT * FROM user");
            ResultSet r=s.executeQuery();
            while (r.next()){
                Object[] data={
                    r.getString(1),
                    r.getString(2),
                    r.getString(3),
                    r.getString(4),
                    r.getString(5),
                    r.getString(6)
                };
                tm.addRow(data);
            }
        } catch (Exception e){
            System.out.print("ERROR KUERI KE DATABASE :\n" +e+ "\n\n");
        }
    }

