    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Anda yakin ingin menghapus data ini='" + txtid.getText()+ "'",".:: konfirmasi ::.", JOptionPane.YES_NO_OPTION);
        if (ok==0){
            try {
                PreparedStatement ps = con.prepareStatement("DELETE FROM outlet WHERE id_outlet=?");
                ps.setString(1, txtid.getText());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data telah di delete");
                tampil_tabel();
            } catch(Exception e) {
                System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        try{
            PreparedStatement ps = con.prepareStatement("UPDATE outlet SET nama=?, alamat=?, tlp=? WHERE id_outlet=?");
            ps.setString(1, txtnama.getText());
            ps.setString(2, txtalamat.getText());
            ps.setString(3, txttelp.getText());
            ps.setString(4, txtid.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data telah di update");
            tampil_tabel();
            
        }catch(Exception e){
            System.out.print("ERROR KUERI KE DATABASE:\n"+ e +"\n\n");
        }
    }//GEN-LAST:event_btneditActionPerformed

    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        try{
            PreparedStatement ps = con.prepareStatement("INSERT INTO outlet VALUES (?,?,?,?)");
            ps.setString(1, txtid.getText());
            ps.setString(2, txtnama.getText());
            ps.setString(3, txtalamat.getText());
            ps.setString(4, txttelp.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data telah tersimpan");
            tampil_tabel();
        }catch(Exception e){
            System.out.print("ERROR KUERI KE DATABASE:\n"+ e +"\n\n");
        }
    }//GEN-LAST:event_btnsaveActionPerformed

    private void tbloutletMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbloutletMouseClicked
        // TODO add your handling code here:
        int i = tbloutlet.getSelectedRow();
        if (i == -1){
            return;
        }
        String id = (String) tm.getValueAt(i, 0);
        txtid.setText(id);
        String nama = (String) tm.getValueAt(i, 1);
        txtnama.setText(nama);
        String alamat = (String) tm.getValueAt(i, 2);
        txtalamat.setText(alamat);
        String telp = (String) tm.getValueAt(i, 3);
        txttelp.setText(telp);
    }//GEN-LAST:event_tbloutletMouseClicked

